import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var paleGrey: UIColor {
        return UIColor(red: 236.0 / 255.0, green: 240.0 / 255.0, blue: 241.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var white: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
}
